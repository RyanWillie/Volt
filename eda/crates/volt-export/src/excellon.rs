//! Excellon drill file export for Volt PCB boards.
//!
//! Generates industry-standard Excellon drill files (`.drl`) for PCB
//! fabrication. Two kinds of files are produced:
//!
//! - **PTH** — plated through-holes: vias and THT pad holes
//! - **NPTH** — non-plated holes: mounting holes, tooling holes, cutouts
//!
//! The Excellon format is a simple text format understood by virtually
//! every CNC drill machine and every board fabrication shop. Volt emits
//! the metric variant with trailing-zero suppression and coordinates
//! expressed in microns (µm) as signed integers.

use std::path::{Path, PathBuf};

use crate::gerber::{transform_point, BoardLibrary};
use volt_core::common::PadSide;
use volt_core::project::{Board, FabricationOutputSettings};

// ═══════════════════════════════════════════════════════════════════════════
// ExcellonWriter — low-level Excellon format writer
// ═══════════════════════════════════════════════════════════════════════════

/// A single drill tool definition (diameter in millimetres).
#[derive(Debug, Clone, Copy)]
struct DrillTool {
    diameter: f64,
}

/// Tolerance used when matching existing tools by diameter (millimetres).
const TOOL_MATCH_TOLERANCE_MM: f64 = 0.001;

/// Low-level Excellon file writer.
///
/// Accumulates tool definitions and drill hits, then emits a complete
/// Excellon file via [`ExcellonWriter::to_string`].
pub struct ExcellonWriter {
    tools: Vec<DrillTool>,
    /// Each hit is `(tool_index, x_mm, y_mm)` where `tool_index` is the
    /// 1-based tool number (T1, T2, ...).
    hits: Vec<(usize, f64, f64)>,
}

impl ExcellonWriter {
    /// Create an empty writer with no tools and no hits.
    pub fn new() -> Self {
        Self {
            tools: Vec::new(),
            hits: Vec::new(),
        }
    }

    /// Register a drill tool with the given diameter (in millimetres) and
    /// return its 1-based tool number.
    pub fn add_tool(&mut self, diameter: f64) -> usize {
        self.tools.push(DrillTool { diameter });
        self.tools.len()
    }

    /// Find an existing tool whose diameter matches `diameter` within
    /// 0.001 mm; otherwise, create a new tool. Returns the 1-based tool
    /// number in either case.
    pub fn find_or_add_tool(&mut self, diameter: f64) -> usize {
        for (i, tool) in self.tools.iter().enumerate() {
            if (tool.diameter - diameter).abs() <= TOOL_MATCH_TOLERANCE_MM {
                return i + 1;
            }
        }
        self.add_tool(diameter)
    }

    /// Record a drill hit using the given 1-based tool number.
    pub fn add_hit(&mut self, tool: usize, x: f64, y: f64) {
        self.hits.push((tool, x, y));
    }

    /// Number of registered tools.
    pub fn tool_count(&self) -> usize {
        self.tools.len()
    }

    /// Number of drill hits recorded.
    pub fn hit_count(&self) -> usize {
        self.hits.len()
    }

    /// Generate the full Excellon file content as a string.
    ///
    /// The output conforms to the metric variant with trailing zero
    /// suppression (`METRIC,TZ`). Coordinates are emitted as signed
    /// integers in microns (mm × 1000).
    pub fn to_string(&self) -> String {
        let mut out = String::with_capacity(256 + 24 * self.hits.len());

        // ── Header ──────────────────────────────────────────────────────
        out.push_str("M48\n");
        out.push_str("; Generated by Volt EDA\n");
        out.push_str("METRIC,TZ\n");
        out.push_str("FMAT,2\n");
        for (i, tool) in self.tools.iter().enumerate() {
            // T{n}C{diameter}
            out.push_str(&format!("T{}C{:.3}\n", i + 1, tool.diameter));
        }
        out.push_str("%\n");

        // ── Body: group hits by tool, in tool order ─────────────────────
        // Group hits by tool so each tool appears once per run.
        let mut current_tool: Option<usize> = None;
        for tool_idx in 1..=self.tools.len() {
            for (t, x, y) in &self.hits {
                if *t != tool_idx {
                    continue;
                }
                if current_tool != Some(tool_idx) {
                    out.push_str(&format!("T{}\n", tool_idx));
                    current_tool = Some(tool_idx);
                }
                out.push_str(&format!("X{}Y{}\n", format_coord(*x), format_coord(*y)));
            }
        }

        // ── Footer ──────────────────────────────────────────────────────
        out.push_str("M30\n");
        out
    }
}

impl Default for ExcellonWriter {
    fn default() -> Self {
        Self::new()
    }
}

/// Convert a millimetre value to an Excellon coordinate string
/// (signed integer in microns; trailing zeros retained).
fn format_coord(mm: f64) -> String {
    let value = (mm * 1000.0).round() as i64;
    value.to_string()
}

