// Hit collection helpers
// ═══════════════════════════════════════════════════════════════════════════

/// A drill hit collected from board data, prior to tool grouping.
#[derive(Debug, Clone, Copy)]
struct RawHit {
    diameter: f64,
    x: f64,
    y: f64,
}

/// Collect all plated through-hole drill hits from a board:
/// - Vias (from every net segment)
/// - THT pad holes on footprint pads (side = ThroughHole)
fn collect_pth_hits(board: &Board, library: &dyn BoardLibrary) -> Result<Vec<RawHit>, String> {
    let mut hits: Vec<RawHit> = Vec::new();

    // ── Vias ────────────────────────────────────────────────────────────
    for seg in &board.net_segments {
        for via in &seg.vias {
            if via.drill <= 0.0 {
                continue;
            }
            hits.push(RawHit {
                diameter: via.drill,
                x: via.position.x,
                y: via.position.y,
            });
        }
    }

    // ── THT pad holes on device footprints ──────────────────────────────
    for dev in &board.devices {
        let Some(lib_dev) = library.get_device(&dev.lib_device) else {
            return Err(format!(
                "library device not found for board device {}",
                dev.component
            ));
        };
        let Some(pkg) = library.get_package(&lib_dev.package) else {
            return Err(format!("library package {} not found", lib_dev.package));
        };
        let Some(footprint) = pkg
            .footprints
            .iter()
            .find(|f| f.uuid == dev.lib_footprint)
        else {
            return Err(format!(
                "footprint {} not found in package",
                dev.lib_footprint
            ));
        };
        for fp_pad in &footprint.pads {
            // Only THT pads have plated drill holes.
            if fp_pad.side != PadSide::ThroughHole {
                continue;
            }
            for hole in &fp_pad.holes {
                if hole.diameter <= 0.0 {
                    continue;
                }
                // Hole location: if the hole has a path, use its first
                // vertex; otherwise, the pad centre. All footprint-local
                // positions must be transformed into board coordinates.
                let (local_x, local_y) = if let Some(first) = hole.path.first() {
                    (
                        fp_pad.position.x + first.position.x,
                        fp_pad.position.y + first.position.y,
                    )
                } else {
                    (fp_pad.position.x, fp_pad.position.y)
                };
                let (x, y) = transform_point(local_x, local_y, dev);
                hits.push(RawHit {
                    diameter: hole.diameter,
                    x,
                    y,
                });
            }
        }
    }

    Ok(hits)
}

/// Collect all non-plated through-hole drill hits from a board's
/// standalone `holes` list.
fn collect_npth_hits(board: &Board) -> Vec<RawHit> {
    let mut hits = Vec::new();
    for hole in &board.holes {
        if hole.diameter <= 0.0 {
            continue;
        }
        // Use the first vertex of the hole path as its position. A hole
        // with no path is ignored (invalid geometry).
        let Some(first) = hole.path.first() else {
            continue;
        };
        hits.push(RawHit {
            diameter: hole.diameter,
            x: first.position.x,
            y: first.position.y,
        });
    }
    hits
}

/// Build an [`ExcellonWriter`] from a list of raw hits, grouping by
/// diameter into tools sorted in ascending order.
fn build_writer(hits: &[RawHit]) -> ExcellonWriter {
    // Collect unique diameters (within tolerance) and sort ascending.
    let mut diameters: Vec<f64> = Vec::new();
    'outer: for h in hits {
        for d in &diameters {
            if (h.diameter - d).abs() <= TOOL_MATCH_TOLERANCE_MM {
                continue 'outer;
            }
        }
        diameters.push(h.diameter);
    }
    diameters.sort_by(|a, b| a.partial_cmp(b).unwrap_or(std::cmp::Ordering::Equal));

    let mut writer = ExcellonWriter::new();
    for d in &diameters {
        writer.add_tool(*d);
    }

    // Assign each hit to its matching tool.
    for h in hits {
        let tool = writer.find_or_add_tool(h.diameter);
        writer.add_hit(tool, h.x, h.y);
    }
    writer
}

// ═══════════════════════════════════════════════════════════════════════════
// Public export API
// ═══════════════════════════════════════════════════════════════════════════

/// Export the PTH (plated through-hole) drill file as an Excellon string.
///
/// Includes every via on the board (outer through vias and blind/buried
/// vias alike — drilling is independent of layer span for this purpose)
/// and every drill hole on every THT footprint pad.
pub fn export_pth_drills(
    board: &Board,
    library: &dyn BoardLibrary,
) -> Result<String, String> {
    let hits = collect_pth_hits(board, library)?;
    let writer = build_writer(&hits);
    Ok(writer.to_string())
}

/// Export the NPTH (non-plated) drill file as an Excellon string.
///
/// Includes every entry in `board.holes` (mounting holes, tooling
/// holes, cutouts, etc.).
pub fn export_npth_drills(board: &Board) -> Result<String, String> {
    let hits = collect_npth_hits(board);
    let writer = build_writer(&hits);
    Ok(writer.to_string())
}

/// Export a merged drill file containing both plated and non-plated
/// holes. The merged file does not distinguish between the two kinds
/// of holes; if a fabricator requires separate PTH/NPTH files, leave
/// `merge_drills` disabled.
pub fn export_merged_drills(
    board: &Board,
    library: &dyn BoardLibrary,
) -> Result<String, String> {
    let mut hits = collect_pth_hits(board, library)?;
    hits.extend(collect_npth_hits(board));
    Ok(build_writer(&hits).to_string())
}

