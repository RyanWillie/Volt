/// Export a silkscreen (legend) layer as a Gerber RS-274X string.
///
/// Renders footprint outlines and board-level polygons on the legend layer.
pub fn export_silkscreen_layer(
    board: &Board,
    _circuit: &Circuit,
    library: &dyn BoardLibrary,
    top: bool,
) -> Result<String> {
    let file_function = if top { "Legend,Top" } else { "Legend,Bot" };
    let mut writer = GerberWriter::new_x2(file_function, "Positive");
    let mut cache = ApertureCache::new();

    let target_layer = if top {
        Layer::TopLegend
    } else {
        Layer::BottomLegend
    };

    let side_name = if top { "Top" } else { "Bottom" };
    writer.comment(&format!("Silkscreen {}", side_name));
    writer.set_polarity_dark();

    // Footprint polygons on legend layers
    for dev in &board.devices {
        let Some(lib_dev) = library.get_device(&dev.lib_device) else {
            continue;
        };
        let Some(pkg) = library.get_package(&lib_dev.package) else {
            continue;
        };
        let Some(footprint) = pkg.footprints.iter().find(|f| f.uuid == dev.lib_footprint) else {
            continue;
        };
        for poly in &footprint.polygons {
            // Account for device flip when mapping layers.
            let poly_layer = if dev.flip {
                match poly.layer {
                    Layer::TopLegend => Layer::BottomLegend,
                    Layer::BottomLegend => Layer::TopLegend,
                    other => other,
                }
            } else {
                poly.layer
            };
            if poly_layer != target_layer {
                continue;
            }
            if poly.vertices.len() < 2 {
                continue;
            }
            let line_width = if poly.width > 0.0 { poly.width } else { 0.15 };
            let ap = cache.circle(&mut writer, line_width);
            writer.select_aperture(ap);

            let first = transform_point(
                poly.vertices[0].position.x,
                poly.vertices[0].position.y,
                dev,
            );
            writer.move_to(first.0, first.1);
            for v in &poly.vertices[1..] {
                let (x, y) = transform_point(v.position.x, v.position.y, dev);
                writer.line_to(x, y);
            }
            // Close polygon
            if poly.vertices.len() > 2 {
                writer.line_to(first.0, first.1);
            }
        }
    }

    // Board-level polygons on legend layers
    for poly in &board.polygons {
        if poly.layer != target_layer {
            continue;
        }
        if poly.vertices.len() < 2 {
            continue;
        }
        let line_width = if poly.width > 0.0 { poly.width } else { 0.15 };
        let ap = cache.circle(&mut writer, line_width);
        writer.select_aperture(ap);

        writer.move_to(poly.vertices[0].position.x, poly.vertices[0].position.y);
        for v in &poly.vertices[1..] {
            writer.line_to(v.position.x, v.position.y);
        }
        if poly.vertices.len() > 2 {
            writer.line_to(poly.vertices[0].position.x, poly.vertices[0].position.y);
        }
    }

    writer.finish();
    Ok(writer.to_string())
}

