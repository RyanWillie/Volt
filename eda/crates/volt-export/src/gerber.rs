// Soldermask layer export
// ═══════════════════════════════════════════════════════════════════════════

/// Export a soldermask layer (mask openings) as a Gerber RS-274X string.
///
/// Produces openings for pads (expanded by stopmask clearance) and for
/// exposed vias.
pub fn export_soldermask_layer(
    board: &Board,
    _circuit: &Circuit,
    library: &dyn BoardLibrary,
    top: bool,
) -> Result<String> {
    let file_function = if top {
        "Soldermask,Top"
    } else {
        "Soldermask,Bot"
    };
    let mut writer = GerberWriter::new_x2(file_function, "Negative");
    let mut cache = ApertureCache::new();
    let mut flashes: Vec<(usize, f64, f64)> = Vec::new();

    let side_name = if top { "Top" } else { "Bottom" };
    writer.comment(&format!("Soldermask {}", side_name));

    // Device footprint pads
    for dev in &board.devices {
        let Some(lib_dev) = library.get_device(&dev.lib_device) else {
            continue;
        };
        let Some(pkg) = library.get_package(&lib_dev.package) else {
            continue;
        };
        let Some(footprint) = pkg.footprints.iter().find(|f| f.uuid == dev.lib_footprint) else {
            continue;
        };
        for fp_pad in &footprint.pads {
            if !pad_on_soldermask(fp_pad.side, dev.flip, top) {
                continue;
            }
            let Some(clearance) = stopmask_clearance_fp(fp_pad, &board.design_rules) else {
                continue;
            };
            let (x, y) = transform_point(fp_pad.position.x, fp_pad.position.y, dev);
            let w = fp_pad.width + 2.0 * clearance;
            let h = fp_pad.height + 2.0 * clearance;
            let ap = match fp_pad.shape {
                PadShape::Round => cache.circle(&mut writer, w),
                _ => cache.rectangle(&mut writer, w, h),
            };
            flashes.push((ap, x, y));
        }
    }

    // Standalone board pads
    for seg in &board.net_segments {
        for pad in &seg.pads {
            if !pad_on_soldermask(pad.side, false, top) {
                continue;
            }
            let Some(clearance) = stopmask_clearance_board_pad(pad, &board.design_rules) else {
                continue;
            };
            let w = pad.size_width + 2.0 * clearance;
            let h = pad.size_height + 2.0 * clearance;
            let ap = match pad.shape {
                PadShape::Round => cache.circle(&mut writer, w),
                _ => cache.rectangle(&mut writer, w, h),
            };
            flashes.push((ap, pad.position.x, pad.position.y));
        }
    }

    // Via mask openings (only if exposed / not tented)
    for seg in &board.net_segments {
        for via in &seg.vias {
            // Check if the via reaches this soldermask side.
            let reaches = if top {
                via.from_layer == Layer::TopCopper || via.to_layer == Layer::TopCopper
            } else {
                via.from_layer == Layer::BottomCopper || via.to_layer == Layer::BottomCopper
            };
            if !reaches {
                continue;
            }
            let Some(clearance) = via_exposure_clearance(via, &board.drc_settings) else {
                continue;
            };
            let via_diameter = compute_via_diameter(via, &board.design_rules);
            let opening = via_diameter + 2.0 * clearance;
            let ap = cache.circle(&mut writer, opening);
            flashes.push((ap, via.position.x, via.position.y));
        }
    }

    // Draw all mask openings
    writer.set_polarity_dark();
    for (ap, x, y) in &flashes {
        writer.select_aperture(*ap);
        writer.flash(*x, *y);
    }

    writer.finish();
    Ok(writer.to_string())
}

