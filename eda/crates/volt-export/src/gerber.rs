/// A Gerber aperture definition.
#[derive(Debug, Clone)]
pub enum Aperture {
    Circle { diameter: f64 },
    Rectangle { width: f64, height: f64 },
}

// ═══════════════════════════════════════════════════════════════════════════
// GerberWriter — low-level RS-274X writer
// ═══════════════════════════════════════════════════════════════════════════

/// Low-level Gerber RS-274X format writer.
///
/// Accumulates Gerber commands into an internal buffer. Coordinates are
/// specified in millimeters and converted to the Gerber integer format
/// using the configured coordinate format (default: 4.6).
pub struct GerberWriter {
    buf: String,
    /// Number of integer digits in coordinate format.
    #[allow(dead_code)]
    int_digits: u8,
    /// Number of decimal digits in coordinate format.
    dec_digits: u8,
    apertures: Vec<Aperture>,
    current_aperture: Option<usize>,
}

impl GerberWriter {
    /// Create a new writer with the standard RS-274X header.
    ///
    /// Emits: format spec (4.6), millimetre units, positive image polarity.
    pub fn new() -> Self {
        let now = chrono::Utc::now().format("%Y-%m-%dT%H:%M:%SZ");
        let mut buf = String::with_capacity(4096);
        buf.push_str(&format!("G04 Generated by Volt EDA on {}*\n", now));
        buf.push_str("%FSLAX46Y46*%\n");
        buf.push_str("%MOMM*%\n");
        buf.push_str("%IPPOS*%\n");
        Self {
            buf,
            int_digits: 4,
            dec_digits: 6,
            apertures: Vec::new(),
            current_aperture: None,
        }
    }

    /// Create a new writer with Gerber X2 attributes in the header.
    ///
    /// Adds %TF.GenerationSoftware, %TF.CreationDate, %TF.FileFunction,
    /// %TF.FilePolarity, and %TF.Part attributes per Ucamco spec.
    pub fn new_x2(file_function: &str, file_polarity: &str) -> Self {
        let now = chrono::Utc::now();
        let date_str = now.format("%Y-%m-%dT%H:%M:%SZ");
        let mut buf = String::with_capacity(4096);
        buf.push_str(&format!("G04 Generated by Volt EDA on {}*\n", date_str));
        buf.push_str("%TF.GenerationSoftware,Volt,volt-eda,0.1*%\n");
        buf.push_str(&format!("%TF.CreationDate,{}*%\n", date_str));
        buf.push_str(&format!("%TF.FileFunction,{}*%\n", file_function));
        buf.push_str(&format!("%TF.FilePolarity,{}*%\n", file_polarity));
        buf.push_str("%TF.Part,Single*%\n");
        buf.push_str("%FSLAX46Y46*%\n");
        buf.push_str("%MOMM*%\n");
        buf.push_str("%IPPOS*%\n");
        Self {
            buf,
            int_digits: 4,
            dec_digits: 6,
            apertures: Vec::new(),
            current_aperture: None,
        }
    }

    /// Register an aperture. Returns its index (used with [`select_aperture`]).
    ///
    /// D-codes start at D10. The aperture definition is emitted immediately.
    pub fn add_aperture(&mut self, aperture: Aperture) -> usize {
        let index = self.apertures.len();
        let d_code = index + 10;
        match &aperture {
            Aperture::Circle { diameter } => {
                self.buf
                    .push_str(&format!("%ADD{}C,{:.6}*%\n", d_code, diameter));
            }
            Aperture::Rectangle { width, height } => {
                self.buf
                    .push_str(&format!("%ADD{}R,{:.6}X{:.6}*%\n", d_code, width, height));
            }
        }
        self.apertures.push(aperture);
        index
    }

    /// Select an aperture by index. Emits `D{code}*` only if different
    /// from the currently selected aperture.
    pub fn select_aperture(&mut self, index: usize) {
        if self.current_aperture != Some(index) {
            let d_code = index + 10;
            self.buf.push_str(&format!("D{}*\n", d_code));
            self.current_aperture = Some(index);
        }
    }

    /// Move to position without drawing (D02).
    pub fn move_to(&mut self, x: f64, y: f64) {
        let cx = self.format_coord(x);
        let cy = self.format_coord(y);
        self.buf.push_str(&format!("X{}Y{}D02*\n", cx, cy));
    }

    /// Draw a line to position (D01).
    pub fn line_to(&mut self, x: f64, y: f64) {
        let cx = self.format_coord(x);
        let cy = self.format_coord(y);
        self.buf.push_str(&format!("X{}Y{}D01*\n", cx, cy));
    }

    /// Flash the current aperture at position (D03).
    pub fn flash(&mut self, x: f64, y: f64) {
        let cx = self.format_coord(x);
        let cy = self.format_coord(y);
        self.buf.push_str(&format!("X{}Y{}D03*\n", cx, cy));
    }

    /// Set layer polarity to dark (`%LPD*%`).
    pub fn set_polarity_dark(&mut self) {
        self.buf.push_str("%LPD*%\n");
    }

    /// Set layer polarity to clear (`%LPC*%`).
    pub fn set_polarity_clear(&mut self) {
        self.buf.push_str("%LPC*%\n");
    }

    /// Begin a filled region (`G36*`).
    pub fn region_start(&mut self) {
        self.buf.push_str("G36*\n");
    }

    /// End a filled region (`G37*`).
    pub fn region_end(&mut self) {
        self.buf.push_str("G37*\n");
    }

    /// Emit an end-of-file command (`M02*`).
    pub fn finish(&mut self) {
        self.buf.push_str("M02*\n");
    }

    /// Add a comment line (`G04`).
    pub fn comment(&mut self, text: &str) {
        self.buf.push_str(&format!("G04 {}*\n", text));
    }

    /// Convert a millimetre value to a Gerber coordinate string.
    ///
    /// Multiplies by 10^dec_digits and rounds to the nearest integer.
    fn format_coord(&self, mm: f64) -> String {
        let scale = 10f64.powi(self.dec_digits as i32);
        let value = (mm * scale).round() as i64;
        format!("{}", value)
    }
}

impl fmt::Display for GerberWriter {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "{}", self.buf)
    }
}

impl Default for GerberWriter {
    fn default() -> Self {
        Self::new()
    }
}

