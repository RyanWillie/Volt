// Board geometry lookup maps
// ═══════════════════════════════════════════════════════════════════════════

/// Pre-computed lookup maps for resolving trace endpoints.
struct BoardMaps<'a> {
    via_map: HashMap<Uuid, &'a Via>,
    junction_map: HashMap<Uuid, Position>,
    /// ComponentInstance UUID → BoardDevice
    device_map: HashMap<Uuid, &'a BoardDevice>,
}

impl<'a> BoardMaps<'a> {
    fn build(board: &'a Board) -> Self {
        let mut via_map = HashMap::new();
        let mut junction_map = HashMap::new();
        for seg in &board.net_segments {
            for via in &seg.vias {
                via_map.insert(via.uuid, via);
            }
            for junc in &seg.junctions {
                junction_map.insert(junc.uuid, junc.position);
            }
        }
        let mut device_map = HashMap::new();
        for dev in &board.devices {
            device_map.insert(dev.component, dev);
        }
        Self {
            via_map,
            junction_map,
            device_map,
        }
    }

    /// Resolve a trace endpoint to board-space (x, y) coordinates.
    fn resolve_endpoint(
        &self,
        endpoint: &TraceEndpoint,
        library: &dyn BoardLibrary,
    ) -> Result<(f64, f64)> {
        match endpoint {
            TraceEndpoint::Via { via } => {
                let v = self.via_map.get(via).ok_or(GerberError::MissingVia(*via))?;
                Ok((v.position.x, v.position.y))
            }
            TraceEndpoint::Junction { junction } => {
                let pos = self
                    .junction_map
                    .get(junction)
                    .ok_or(GerberError::MissingJunction(*junction))?;
                Ok((pos.x, pos.y))
            }
            TraceEndpoint::Device { device, pad } => {
                let board_dev = self
                    .device_map
                    .get(device)
                    .ok_or(GerberError::MissingDevice(*device))?;
                let lib_dev = library
                    .get_device(&board_dev.lib_device)
                    .ok_or(GerberError::MissingDevice(board_dev.lib_device))?;
                let pkg = library
                    .get_package(&lib_dev.package)
                    .ok_or(GerberError::MissingPackage(lib_dev.package))?;
                let footprint = pkg
                    .footprints
                    .iter()
                    .find(|f| f.uuid == board_dev.lib_footprint)
                    .ok_or(GerberError::MissingFootprint(board_dev.lib_footprint))?;
                let fp_pad = footprint
                    .pads
                    .iter()
                    .find(|p| p.uuid == *pad || p.package_pad == *pad)
                    .ok_or(GerberError::MissingPad(*pad))?;
                Ok(transform_point(
                    fp_pad.position.x,
                    fp_pad.position.y,
                    board_dev,
                ))
            }
        }
    }
}

// ═══════════════════════════════════════════════════════════════════════════
// Copper layer export
// ═══════════════════════════════════════════════════════════════════════════

/// Export a single copper layer as a Gerber RS-274X string.
///
/// Includes traces, device pads, standalone pads, vias, and copper planes.
pub fn export_copper_layer(
    board: &Board,
    _circuit: &Circuit,
    library: &dyn BoardLibrary,
    layer: Layer,
) -> Result<String> {
    let file_function = match layer {
        Layer::TopCopper => "Copper,L1,Top".to_string(),
        Layer::BottomCopper => "Copper,L2,Bot".to_string(),
        Layer::InnerCopper(n) => format!("Copper,L{},Inr", n + 1),
        _ => "Copper,L1,Top".to_string(),
    };
    let mut writer = GerberWriter::new_x2(&file_function, "Positive");
    let mut cache = ApertureCache::new();
    let maps = BoardMaps::build(board);

    writer.comment(&format!("Layer: {:?}", layer));

    // ── Collect geometry ─────────────────────────────────────────────────

    // Traces
    let mut trace_draws: Vec<(usize, (f64, f64), (f64, f64))> = Vec::new();
    for seg in &board.net_segments {
        for trace in &seg.traces {
            if trace.layer != layer {
                continue;
            }
            let from = maps.resolve_endpoint(&trace.from, library)?;
            let to = maps.resolve_endpoint(&trace.to, library)?;
            let ap = cache.circle(&mut writer, trace.width);
            trace_draws.push((ap, from, to));
        }
    }

    // Device footprint pads
    let mut pad_flashes: Vec<(usize, f64, f64)> = Vec::new();
    for dev in &board.devices {
        let Some(lib_dev) = library.get_device(&dev.lib_device) else {
            continue;
        };
        let Some(pkg) = library.get_package(&lib_dev.package) else {
            continue;
        };
        let Some(footprint) = pkg.footprints.iter().find(|f| f.uuid == dev.lib_footprint) else {
            continue;
        };
        for fp_pad in &footprint.pads {
            if !pad_on_copper_layer(fp_pad.side, dev.flip, layer) {
                continue;
            }
            let (x, y) = transform_point(fp_pad.position.x, fp_pad.position.y, dev);
            let ap = match fp_pad.shape {
                PadShape::Round => cache.circle(&mut writer, fp_pad.width),
                _ => cache.rectangle(&mut writer, fp_pad.width, fp_pad.height),
            };
            pad_flashes.push((ap, x, y));
        }
    }

    // Standalone board pads
    for seg in &board.net_segments {
        for pad in &seg.pads {
            if !pad_on_copper_layer(pad.side, false, layer) {
                continue;
            }
            let ap = match pad.shape {
                PadShape::Round => cache.circle(&mut writer, pad.size_width),
                _ => cache.rectangle(&mut writer, pad.size_width, pad.size_height),
            };
            pad_flashes.push((ap, pad.position.x, pad.position.y));
        }
    }

    // Vias
    let mut via_flashes: Vec<(usize, f64, f64)> = Vec::new();
    for seg in &board.net_segments {
        for via in &seg.vias {
            if !via_on_layer(via, layer) {
                continue;
            }
            let diameter = compute_via_diameter(via, &board.design_rules);
            let ap = cache.circle(&mut writer, diameter);
            via_flashes.push((ap, via.position.x, via.position.y));
        }
    }

    // ── Draw ─────────────────────────────────────────────────────────────

    writer.set_polarity_dark();

    // Traces
    for (ap, from, to) in &trace_draws {
        writer.select_aperture(*ap);
        writer.move_to(from.0, from.1);
        writer.line_to(to.0, to.1);
    }

    // Pads
    for (ap, x, y) in &pad_flashes {
        writer.select_aperture(*ap);
        writer.flash(*x, *y);
    }

    // Vias
    for (ap, x, y) in &via_flashes {
        writer.select_aperture(*ap);
        writer.flash(*x, *y);
    }

    // Copper planes (filled regions from refill fragments)
    for plane in &board.planes {
        if plane.layer != layer {
            continue;
        }
        if !plane.fragments.is_empty() {
            // Use computed refill fragments
            for fragment in &plane.fragments {
                if fragment.contours.is_empty() {
                    continue;
                }
                // Outer boundary = dark region
                let outer = &fragment.contours[0];
                if outer.len() >= 3 {
                    writer.region_start();
                    writer.move_to(outer[0].x, outer[0].y);
                    for pt in &outer[1..] {
                        writer.line_to(pt.x, pt.y);
                    }
                    writer.line_to(outer[0].x, outer[0].y);
                    writer.region_end();
                }
                // Holes = clear regions
                for hole in &fragment.contours[1..] {
                    if hole.len() >= 3 {
                        writer.set_polarity_clear();
                        writer.region_start();
                        writer.move_to(hole[0].x, hole[0].y);
                        for pt in &hole[1..] {
                            writer.line_to(pt.x, pt.y);
                        }
                        writer.line_to(hole[0].x, hole[0].y);
                        writer.region_end();
                        writer.set_polarity_dark();
                    }
                }
            }
        } else if plane.vertices.len() >= 3 {
            // Fallback: raw plane outline (no refill computed)
            writer.region_start();
            let first = &plane.vertices[0];
            writer.move_to(first.position.x, first.position.y);
            for v in &plane.vertices[1..] {
                writer.line_to(v.position.x, v.position.y);
            }
            writer.line_to(first.position.x, first.position.y);
            writer.region_end();
        }
    }

    writer.finish();
    Ok(writer.to_string())
}

