/// Summary of files written by [`export_all_drills`].
#[derive(Debug)]
pub struct DrillSummary {
    pub files: Vec<DrillFile>,
    pub pth_hole_count: usize,
    pub npth_hole_count: usize,
}

/// A single exported drill file.
#[derive(Debug)]
pub struct DrillFile {
    pub path: PathBuf,
    pub kind: DrillFileKind,
    pub hole_count: usize,
}

/// Which kind of drill file was written.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum DrillFileKind {
    Pth,
    Npth,
    Merged,
}

/// Export all drill files for the board to `output_dir`.
///
/// Always writes a PTH file and an NPTH file, using the suffixes
/// `drills_pth_suffix` and `drills_npth_suffix` from the board's
/// [`FabricationOutputSettings`]. If `settings.merge_drills` is true,
/// an additional merged file is written using `drills_merged_suffix`.
///
/// File names are formed as `{board.name}{suffix}`.
pub fn export_all_drills(
    board: &Board,
    library: &dyn BoardLibrary,
    output_dir: &Path,
    settings: &FabricationOutputSettings,
) -> Result<DrillSummary, String> {
    std::fs::create_dir_all(output_dir)
        .map_err(|e| format!("failed to create output directory: {e}"))?;

    // ── PTH ────────────────────────────────────────────────────────────
    let pth_hits = collect_pth_hits(board, library)?;
    let pth_content = build_writer(&pth_hits).to_string();
    let pth_path = output_dir.join(format!("{}{}", board.name, settings.drills_pth_suffix));
    std::fs::write(&pth_path, &pth_content)
        .map_err(|e| format!("failed to write PTH drill file: {e}"))?;

    // ── NPTH ────────────────────────────────────────────────────────────
    let npth_hits = collect_npth_hits(board);
    let npth_content = build_writer(&npth_hits).to_string();
    let npth_path = output_dir.join(format!("{}{}", board.name, settings.drills_npth_suffix));
    std::fs::write(&npth_path, &npth_content)
        .map_err(|e| format!("failed to write NPTH drill file: {e}"))?;

    let mut files = vec![
        DrillFile {
            path: pth_path,
            kind: DrillFileKind::Pth,
            hole_count: pth_hits.len(),
        },
        DrillFile {
            path: npth_path,
            kind: DrillFileKind::Npth,
            hole_count: npth_hits.len(),
        },
    ];

    // ── Merged (optional) ───────────────────────────────────────────────
    if settings.merge_drills {
        let mut all_hits = pth_hits.clone();
        all_hits.extend(npth_hits.clone());
        let merged_content = build_writer(&all_hits).to_string();
        let merged_path =
            output_dir.join(format!("{}{}", board.name, settings.drills_merged_suffix));
        std::fs::write(&merged_path, &merged_content)
            .map_err(|e| format!("failed to write merged drill file: {e}"))?;
        files.push(DrillFile {
            path: merged_path,
            kind: DrillFileKind::Merged,
            hole_count: all_hits.len(),
        });
    }

    Ok(DrillSummary {
        files,
        pth_hole_count: pth_hits.len(),
        npth_hole_count: npth_hits.len(),
    })
}

