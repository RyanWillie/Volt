// Aperture cache — deduplicates aperture definitions
// ═══════════════════════════════════════════════════════════════════════════

#[derive(Debug, Clone, PartialEq, Eq, Hash)]
enum ApertureKey {
    Circle { diameter_nm: i64 },
    Rectangle { width_nm: i64, height_nm: i64 },
}

fn mm_to_nm(mm: f64) -> i64 {
    (mm * 1_000_000.0).round() as i64
}

struct ApertureCache {
    map: HashMap<ApertureKey, usize>,
}

impl ApertureCache {
    fn new() -> Self {
        Self {
            map: HashMap::new(),
        }
    }

    fn circle(&mut self, writer: &mut GerberWriter, diameter: f64) -> usize {
        let key = ApertureKey::Circle {
            diameter_nm: mm_to_nm(diameter),
        };
        if let Some(&idx) = self.map.get(&key) {
            return idx;
        }
        let idx = writer.add_aperture(Aperture::Circle { diameter });
        self.map.insert(key, idx);
        idx
    }

    fn rectangle(&mut self, writer: &mut GerberWriter, width: f64, height: f64) -> usize {
        let key = ApertureKey::Rectangle {
            width_nm: mm_to_nm(width),
            height_nm: mm_to_nm(height),
        };
        if let Some(&idx) = self.map.get(&key) {
            return idx;
        }
        let idx = writer.add_aperture(Aperture::Rectangle { width, height });
        self.map.insert(key, idx);
        idx
    }
}

