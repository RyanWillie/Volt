/// Transform a point from footprint-local coordinates to board coordinates,
/// applying device flip, rotation, and translation.
pub fn transform_point(px: f64, py: f64, device: &BoardDevice) -> (f64, f64) {
    let mut x = px;
    let y = py;
    // Mirror X when flipped to the bottom side.
    if device.flip {
        x = -x;
    }
    let angle = device.rotation.0.to_radians();
    let cos = angle.cos();
    let sin = angle.sin();
    let rx = x * cos - y * sin;
    let ry = x * sin + y * cos;
    (rx + device.position.x, ry + device.position.y)
}

/// Determine the effective pad side after accounting for device flip.
fn effective_pad_side(pad_side: PadSide, flipped: bool) -> PadSide {
    if !flipped {
        return pad_side;
    }
    match pad_side {
        PadSide::Top => PadSide::Bottom,
        PadSide::Bottom => PadSide::Top,
        PadSide::ThroughHole => PadSide::ThroughHole,
    }
}

/// Check whether a pad should appear on a given copper layer.
fn pad_on_copper_layer(pad_side: PadSide, flipped: bool, layer: Layer) -> bool {
    let side = effective_pad_side(pad_side, flipped);
    matches!(
        (side, layer),
        (PadSide::Top, Layer::TopCopper)
            | (PadSide::Bottom, Layer::BottomCopper)
            | (PadSide::ThroughHole, Layer::TopCopper)
            | (PadSide::ThroughHole, Layer::BottomCopper)
            | (PadSide::ThroughHole, Layer::InnerCopper(_))
    )
}

/// Check whether a pad should have a soldermask opening on a given side.
fn pad_on_soldermask(pad_side: PadSide, flipped: bool, top: bool) -> bool {
    let side = effective_pad_side(pad_side, flipped);
    matches!(
        (side, top),
        (PadSide::Top, true) | (PadSide::Bottom, false) | (PadSide::ThroughHole, _)
    )
}

/// Return an ordinal number for a copper layer (for range checks on vias).
fn layer_ordinal(layer: Layer) -> Option<i32> {
    match layer {
        Layer::TopCopper => Some(0),
        Layer::InnerCopper(n) => Some(n as i32),
        Layer::BottomCopper => Some(1000),
        _ => None,
    }
}

/// Check whether a via is present on a given copper layer.
fn via_on_layer(via: &Via, layer: Layer) -> bool {
    let lo = layer_ordinal(layer);
    let from = layer_ordinal(via.from_layer);
    let to = layer_ordinal(via.to_layer);
    match (lo, from, to) {
        (Some(l), Some(f), Some(t)) => {
            let (min, max) = if f <= t { (f, t) } else { (t, f) };
            l >= min && l <= max
        }
        _ => false,
    }
}

/// Compute the outer (copper ring) diameter of a via.
fn compute_via_diameter(via: &Via, rules: &DesignRules) -> f64 {
    match via.size {
        ViaSize::Manual(d) => d,
        ViaSize::Auto => {
            let ring = (rules.via_annular_ring_ratio * via.drill)
                .max(rules.via_annular_ring_min)
                .min(rules.via_annular_ring_max);
            via.drill + 2.0 * ring
        }
    }
}

/// Compute the soldermask clearance for a footprint pad, or `None` if the
/// pad has no mask opening.
fn stopmask_clearance_fp(
    pad: &volt_core::library::FootprintPad,
    rules: &DesignRules,
) -> Option<f64> {
    match pad.stop_mask {
        StopMaskConfig::Off => None,
        StopMaskConfig::Manual(c) => Some(c),
        StopMaskConfig::Auto => {
            let max_dim = pad.width.max(pad.height);
            let clearance = (rules.stopmask_clearance_ratio * max_dim)
                .max(rules.stopmask_clearance_min)
                .min(rules.stopmask_clearance_max);
            Some(clearance)
        }
    }
}

/// Compute the soldermask clearance for a standalone board pad, or `None`.
fn stopmask_clearance_board_pad(pad: &BoardPad, rules: &DesignRules) -> Option<f64> {
    match pad.stop_mask {
        StopMaskConfig::Off => None,
        StopMaskConfig::Manual(c) => Some(c),
        StopMaskConfig::Auto => {
            let max_dim = pad.size_width.max(pad.size_height);
            let clearance = (rules.stopmask_clearance_ratio * max_dim)
                .max(rules.stopmask_clearance_min)
                .min(rules.stopmask_clearance_max);
            Some(clearance)
        }
    }
}

/// Return the soldermask exposure clearance for a via, or `None` if tented.
fn via_exposure_clearance(via: &Via, drc: &DrcSettings) -> Option<f64> {
    match via.exposure {
        ViaExposure::Off => None,
        ViaExposure::Manual(clearance) => Some(clearance),
        ViaExposure::Auto => {
            if via.drill <= drc.max_tented_via_drill_diameter {
                None // tented — no mask opening
            } else {
                Some(0.1) // default small clearance for exposed vias
            }
        }
    }
}

