    fn export_all_drills_with_merge() {
        let tmp = std::env::temp_dir().join(format!(
            "volt_excellon_merge_{}",
            Uuid::new_v4().simple()
        ));
        let mut board = empty_board("merged");
        board.net_segments.push(BoardNetSegment {
            uuid: Uuid::new_v4(),
            net: None,
            traces: vec![],
            vias: vec![make_via(0.3, 10.0, 10.0)],
            junctions: vec![],
            pads: vec![],
        });
        board.holes.push(make_hole(3.2, 5.0, 5.0));

        let library = MapBoardLibrary::new();
        let mut settings = FabricationOutputSettings::default();
        settings.merge_drills = true;

        let summary =
            export_all_drills(&board, &library, &tmp, &settings).expect("export ok");
        assert_eq!(summary.files.len(), 3);
        let merged = summary
            .files
            .iter()
            .find(|f| f.kind == DrillFileKind::Merged)
            .expect("merged file present");
        assert_eq!(merged.hole_count, 2);

        let content = std::fs::read_to_string(&merged.path).unwrap();
        // Merged file has both hits; tools sorted ascending → 0.3, 3.2.
        assert!(content.contains("T1C0.300"));
        assert!(content.contains("T2C3.200"));
        assert!(content.contains("X10000Y10000"));
        assert!(content.contains("X5000Y5000"));

        let _ = std::fs::remove_dir_all(&tmp);
    }
