/// Summary of files written by [`export_all`].
#[derive(Debug)]
pub struct ExportSummary {
    pub files: Vec<ExportedFile>,
}

/// A single exported Gerber file.
#[derive(Debug)]
pub struct ExportedFile {
    pub path: PathBuf,
    pub layer_name: String,
}

/// Helper to write a single Gerber file and record it in the summary.
fn write_gerber_file(
    files: &mut Vec<ExportedFile>,
    output_dir: &Path,
    board_name: &str,
    suffix: &str,
    layer_name: &str,
    content: &str,
) -> Result<()> {
    let filename = format!("{}{}", board_name, suffix);
    let path = output_dir.join(filename);
    std::fs::write(&path, content)?;
    files.push(ExportedFile {
        path,
        layer_name: layer_name.to_string(),
    });
    Ok(())
}

/// Export all fabrication layers for a board to the given output directory.
///
/// Writes Gerber files for:
/// - Board outline
/// - Top and bottom copper
/// - Inner copper layers (if any)
/// - Top and bottom soldermask
/// - Top and bottom silkscreen
///
/// File names are formed as `{board.name}{suffix}` using the suffixes
/// from [`FabricationOutputSettings`].
pub fn export_all(
    board: &Board,
    circuit: &Circuit,
    library: &dyn BoardLibrary,
    output_dir: &Path,
) -> Result<ExportSummary> {
    std::fs::create_dir_all(output_dir)?;

    let settings = &board.fabrication_output_settings;
    let name = &board.name;
    let mut files = Vec::new();

    // Board outline
    let content = export_board_outline(board)?;
    write_gerber_file(
        &mut files,
        output_dir,
        name,
        &settings.outlines_suffix,
        "Board Outlines",
        &content,
    )?;

    // Top copper
    let content = export_copper_layer(board, circuit, library, Layer::TopCopper)?;
    write_gerber_file(
        &mut files,
        output_dir,
        name,
        &settings.copper_top_suffix,
        "Top Copper",
        &content,
    )?;

    // Bottom copper
    let content = export_copper_layer(board, circuit, library, Layer::BottomCopper)?;
    write_gerber_file(
        &mut files,
        output_dir,
        name,
        &settings.copper_bot_suffix,
        "Bottom Copper",
        &content,
    )?;

    // Inner copper layers
    for i in 1..=board.inner_layers {
        let layer = Layer::InnerCopper(i);
        let content = export_copper_layer(board, circuit, library, layer)?;
        let suffix = settings
            .copper_inner_suffix
            .replace("{{CU_LAYER}}", &i.to_string());
        let layer_name = format!("Inner Copper {}", i);
        write_gerber_file(&mut files, output_dir, name, &suffix, &layer_name, &content)?;
    }

    // Soldermask
    let content = export_soldermask_layer(board, circuit, library, true)?;
    write_gerber_file(
        &mut files,
        output_dir,
        name,
        &settings.soldermask_top_suffix,
        "Top Soldermask",
        &content,
    )?;

    let content = export_soldermask_layer(board, circuit, library, false)?;
    write_gerber_file(
        &mut files,
        output_dir,
        name,
        &settings.soldermask_bot_suffix,
        "Bottom Soldermask",
        &content,
    )?;

    // Silkscreen
    let content = export_silkscreen_layer(board, circuit, library, true)?;
    write_gerber_file(
        &mut files,
        output_dir,
        name,
        &settings.silkscreen_top_suffix,
        "Top Silkscreen",
        &content,
    )?;

    let content = export_silkscreen_layer(board, circuit, library, false)?;
    write_gerber_file(
        &mut files,
        output_dir,
        name,
        &settings.silkscreen_bot_suffix,
        "Bottom Silkscreen",
        &content,
    )?;

    // Solder paste / stencil
    let content = export_paste_layer(board, circuit, library, true)?;
    write_gerber_file(
        &mut files,
        output_dir,
        name,
        &settings.paste_top_suffix,
        "Top Paste",
        &content,
    )?;

    let content = export_paste_layer(board, circuit, library, false)?;
    write_gerber_file(
        &mut files,
        output_dir,
        name,
        &settings.paste_bot_suffix,
        "Bottom Paste",
        &content,
    )?;

    Ok(ExportSummary { files })
}

