// Error types
// ═══════════════════════════════════════════════════════════════════════════

/// Errors that can occur during Gerber export.
#[derive(Debug, thiserror::Error)]
pub enum GerberError {
    #[error("board device not found for component {0}")]
    MissingDevice(Uuid),
    #[error("package not found: {0}")]
    MissingPackage(Uuid),
    #[error("footprint not found: {0}")]
    MissingFootprint(Uuid),
    #[error("footprint pad not found: {0}")]
    MissingPad(Uuid),
    #[error("via not found: {0}")]
    MissingVia(Uuid),
    #[error("junction not found: {0}")]
    MissingJunction(Uuid),
    #[error("no board outline polygon found")]
    NoBoardOutline,
    #[error("I/O error: {0}")]
    Io(#[from] std::io::Error),
}

/// Result type alias for Gerber operations.
pub type Result<T> = std::result::Result<T, GerberError>;

