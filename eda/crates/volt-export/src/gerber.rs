/// Export the board outline as a Gerber RS-274X string.
///
/// The outline is drawn from the first [`BoardPolygon`] on the
/// [`Layer::BoardOutlines`] layer.
pub fn export_board_outline(board: &Board) -> Result<String> {
    let outline = board
        .polygons
        .iter()
        .find(|p| p.layer == Layer::BoardOutlines)
        .ok_or(GerberError::NoBoardOutline)?;

    if outline.vertices.is_empty() {
        return Err(GerberError::NoBoardOutline);
    }

    let mut writer = GerberWriter::new_x2("Profile,NP", "Positive");
    writer.comment("Board Outline");

    // Use a thin line for the outline routing path.
    let ap = writer.add_aperture(Aperture::Circle { diameter: 0.05 });
    writer.set_polarity_dark();
    writer.select_aperture(ap);

    let first = &outline.vertices[0];
    writer.move_to(first.position.x, first.position.y);
    for v in &outline.vertices[1..] {
        writer.line_to(v.position.x, v.position.y);
    }
    // Close the outline path
    writer.line_to(first.position.x, first.position.y);

    writer.finish();
    Ok(writer.to_string())
}

